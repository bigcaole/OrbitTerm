export const normalizeAppLanguage = (value: unknown): AppLanguage => {
  if (value === 'zh-Hant' || value === 'en' || value === 'ja') {
    return value;
  }
  return 'zh-Hans';
};

export const detectDefaultAppLanguage = (): AppLanguage => {
  if (typeof navigator === 'undefined') {
    return 'zh-Hans';
  }
  const raw = (navigator.language || '').toLowerCase();
  if (raw.startsWith('zh')) {
    if (raw.includes('hant') || raw.includes('tw') || raw.includes('hk') || raw.includes('mo')) {
      return 'zh-Hant';
    }
    return 'zh-Hans';
  }
  if (raw.startsWith('ja')) {
    return 'ja';
  }
  return 'en';
};

const formatTemplate = (template: string, vars?: Record<string, string | number>): string => {
  if (!vars) {
    return template;
  }
  return template.replace(/\{([\w.-]+)\}/g, (_full, key: string) => {
    const value = vars[key];
    return value === undefined ? '' : String(value);
  });
};

export const tWithLanguage = (
  language: AppLanguage,
  key: string,
  vars?: Record<string, string | number>
): string => {
  const safeLanguage = normalizeAppLanguage(language);
  const raw = MESSAGES[safeLanguage][key] ?? MESSAGES['zh-Hans'][key] ?? key;
  return formatTemplate(raw, vars);
};

export const localeTagForLanguage = (language: AppLanguage): string => {
  return LOCALE_BY_LANGUAGE[normalizeAppLanguage(language)] ?? 'zh-CN';
};
